
package eu.simplejson.enums;


public enum JsonType {

    /**
     * This is a string object
     */
    STRING,

    /**
     * This is a number object (double, int or other)
     */
    NUMBER,

    /**
     * A json object
     */
    OBJECT,

    /**
     * A json array
     */
    ARRAY,

    /**
     * A boolean
     */
    BOOLEAN,

    /**
     * A nulled object
     */
    NULL,

    /**
     * An undefined object
     */
    UNDEFINED
}
